package net.ddns.hnmirror.config;

public class Config {
	private String ipAppSrv;
	private int portAppSrv;
	
	public void setIpAppSrv(String ipAppSrv) {
		this.ipAppSrv = ipAppSrv;
	}
	public String getIpAppSrv() {
		return ipAppSrv;
	}
	
	public void setPortAppSrv(int portAppSrv) {
		this.portAppSrv = portAppSrv;
	}
	public int getPortAppSrv() {
		return portAppSrv;
	}
	
}
